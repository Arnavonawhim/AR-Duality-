// ===========================================
// HARD QUESTIONS (Ages 15-18)
// ===========================================
//
// Q1: State Newton's Universal Law of Gravitation.
// Keywords: inversely, proportional, square, distance, masses
// Answer: Every mass attracts every other mass with a force proportional to the product of their masses and inversely proportional to the square of the distance between them.
// Hint: Think about how distance affects gravitational force.
// Points: 20
//
// Q2: What is Einstein's explanation of gravity in General Relativity?
// Keywords: spacetime, curvature, curve, warp, bend
// Answer: Gravity is the curvature of spacetime caused by mass and energy.
// Hint: Einstein said space and time can bend.
// Points: 20
//
// Q3: Calculate the weight of a 50kg person on the Moon (g = 1.6 m/s²).
// Keywords: 80, newtons, weight
// Answer: 80 Newtons (50 kg × 1.6 m/s² = 80 N)
// Hint: Weight = mass × gravity
// Points: 20
//
// Q4: What is terminal velocity?
// Keywords: maximum, constant, air resistance, drag
// Answer: The constant maximum velocity an object reaches when air resistance equals gravitational force.
// Hint: When you stop accelerating while falling.
// Points: 20
//
// Q5: Explain why astronauts float in the International Space Station.
// Keywords: falling, orbit, freefall, microgravity
// Answer: They are in continuous freefall (orbit) around Earth, creating microgravity.
// Hint: They're not actually weightless - they're falling at the same rate as the station.
// Points: 20
