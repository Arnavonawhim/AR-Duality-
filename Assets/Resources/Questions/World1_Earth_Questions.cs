// Q1: What makes things fall down to the ground?
// Keywords: gravity, earth, pull
// Answer: Gravity pulls things down toward the Earth.
// Hint: Think about why a ball comes back down when you throw it up.
// Points: 10
//
// Q2: What do we call the force that pulls you down on a slide?
// Keywords: gravity
// Answer: Gravity!
// Hint: It starts with the letter G.
// Points: 10
//
// Q3: Why does a ball roll downhill?
// Keywords: gravity, pull, down
// Answer: Gravity pulls the ball down the hill.
// Hint: Something is pulling the ball toward the bottom.
// Points: 10
//
// Q4: What happens to a feather and a rock if you drop them?
// Keywords: fall, ground, drop
// Answer: They both fall down, but the rock falls faster because of air.
// Hint: Think about gravity and air.
// Points: 10
//
// Q5: Is the Moon pulled by Earth's gravity?
// Keywords: yes, gravity, pull, orbit
// Answer: Yes! The Moon is held by Earth's gravity.
// Hint: What keeps the Moon from floating away?
// Points: 10
