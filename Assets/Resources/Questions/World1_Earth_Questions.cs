// Q1: Who discovered the laws of gravity?
// Keywords: newton, isaac, sir
// Answer: Sir Isaac Newton discovered the laws of gravity.
// Hint: Legend says an apple fell on his head!
// Points: 15
//
// Q2: What is the formula for force in Newton's second law?
// Keywords: f=ma, force, mass, acceleration
// Answer: F = m × a (Force equals mass times acceleration)
// Hint: Force equals something times acceleration.
// Points: 15
//
// Q3: What is the acceleration due to gravity on Earth?
// Keywords: 9.8, 10, meters, second, squared
// Answer: About 9.8 meters per second squared (m/s²)
// Hint: It's close to 10.
// Points: 15
//
// Q4: What's the difference between mass and weight?
// Keywords: mass, matter, weight, force, gravity
// Answer: Mass is how much matter you have; weight is the force of gravity on that mass.
// Hint: Your mass stays the same on the Moon, but your weight changes.
// Points: 15
//
// Q5: What type of energy does a ball have at the top of a hill?
// Keywords: potential, stored, gravitational
// Answer: Gravitational potential energy.
// Hint: It's stored energy waiting to be released.
// Points: 15
