// HARD QUESTIONS (Ages 15-18)
// ===========================================
//
// Q1: Explain Kepler's First Law of planetary motion.
// Keywords: ellipse, elliptical, orbit, sun, focus
// Answer: Planets orbit the Sun in elliptical paths with the Sun at one focus.
// Hint: Orbits aren't perfect circles.
// Points: 20
//
// Q2: What is the Cosmic Microwave Background radiation?
// Keywords: big bang, afterglow, radiation, early universe
// Answer: The thermal radiation left over from the early universe, about 380,000 years after the Big Bang.
// Hint: It's the "echo" of the Big Bang.
// Points: 20
//
// Q3: What is the Doppler effect and how does it apply to astronomy?
// Keywords: redshift, blueshift, wavelength, frequency, moving
// Answer: The change in wavelength of light from objects moving toward or away from us. Redshift indicates moving away; blueshift indicates approaching.
// Hint: Think about how an ambulance siren changes pitch.
// Points: 20
//
// Q4: What evidence supports the Big Bang theory?
// Keywords: expansion, cmb, microwave, background, redshift, abundance, elements
// Answer: Cosmic microwave background radiation, redshift of galaxies showing expansion, and the abundance of light elements like hydrogen and helium.
// Hint: Three main pieces of evidence.
// Points: 20
//
// Q5: What is dark matter and why do scientists think it exists?
// Keywords: gravity, invisible, mass, galaxies, rotation, gravitational
// Answer: Invisible matter that doesn't emit light but affects gravity. Scientists infer its existence from galaxy rotation curves and gravitational lensing.
// Hint: Galaxies rotate faster than visible matter can explain.
// Points: 20
