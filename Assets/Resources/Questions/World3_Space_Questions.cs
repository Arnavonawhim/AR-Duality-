// MEDIUM QUESTIONS (Ages 11-14)
// ===========================================
//
// Q1: What is a light-year?
// Keywords: distance, light, travels, year
// Answer: The distance light travels in one year (about 9.46 trillion kilometers).
// Hint: It's a distance, not a time!
// Points: 15
//
// Q2: What causes the phases of the Moon?
// Keywords: sun, light, orbit, position, shadow
// Answer: The Moon's changing position relative to Earth and the Sun changes how much of it we see lit up.
// Hint: Think about where the sunlight is hitting.
// Points: 15
//
// Q3: What is a black hole?
// Keywords: gravity, light, escape, collapse, star
// Answer: A region of space where gravity is so strong that nothing, not even light, can escape.
// Hint: It forms when a massive star collapses.
// Points: 15
//
// Q4: Name the gas giants in our solar system.
// Keywords: jupiter, saturn, uranus, neptune
// Answer: Jupiter, Saturn, Uranus, and Neptune.
// Hint: They're the big planets far from the Sun.
// Points: 15
//
// Q5: What is the asteroid belt?
// Keywords: mars, jupiter, rocks, objects
// Answer: A region between Mars and Jupiter filled with rocky objects and asteroids.
// Hint: It's between the 4th and 5th planets.
// Points: 15
//
